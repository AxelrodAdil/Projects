package OOP_Java.ATM_java;

import java.util.Scanner;

public class SpecialForLine40_102 {
    static Scanner sc = new Scanner(System.in);
    public static String StringUse (){
        String string = sc.nextLine();
        return string;
    }

    public static int IntUse (){
        int int_main = sc.nextInt();
        return int_main;
    }
}